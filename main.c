/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef *htim)
{
	if (htim -> Instance == TIM6)
	{
		  HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);										// blinking LED5
		  EncoderValueNew = __HAL_TIM_GET_COUNTER(&htim4); 								// get encoder value
		  EncoderValueDiff = EncoderValueNew - EncoderValueOld;							// encoder value Difference
		  EncoderValueOld = EncoderValueNew;											// previous value = new value
		  drehzahlinRPM =  (0.5 * (EncoderValueDiff / 0.08192 ) * 60 ) / SignaleProUmdrehung;		// 	convert signals of encoder to rotational speed in RPM
		  ClosedLoopDifference = TargetSpeed - drehzahlinRPM;							//  speed difference
// pid controller code
		  esum = esum + ClosedLoopDifference;
		  kp = 50;															// p
		  ki = 35;															// i
		  kd = 0.5;															// d
		  dcPWM = ClosedLoopDifference * kp + esum * ki + (ClosedLoopDifference - ClosedLoopDifferenceOld) * kd;		//duty Cycle
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1 , (uint32_t)dcPWM);				// putting duty cycle in CH1
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2 , 0);							// putting 0 in CH2
		  if (dcPWM > 65536)												// PWM maximum value (TIM3 ARR)
		  {
			  dcPWM = 65536;
			  esum = esum - ClosedLoopDifference;
		  }
		  if (dcPWM < 0)													// PWM minimum value
		  {
			  dcPWM = 0;
			  esum = esum - ClosedLoopDifference;
		  }
		  ClosedLoopDifferenceOld = ClosedLoopDifference;
// values copying to StepResponseBuffer
		  xBuffer[ClosedLoopCNT] = drehzahlinRPM;			// putting values of rotational speed in Array
		  wBuffer[ClosedLoopCNT] = TargetSpeed;			// putting values of target speed in Array
		  eBuffer[ClosedLoopCNT] = ClosedLoopDifference;			// putting values of speed difference in Array
		  yBuffer[ClosedLoopCNT] = dcPWM;			// putting values of duty cycle in Array
		  ClosedLoopCNT += 1;
		  if(ClosedLoopCNT == _BUFFERSIZE)
		  {
// stopping interrupt Timer to send data
		   HAL_TIM_Base_Stop_IT(&htim6);
		  		   //Ausgabe PWM stoppen
		   __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1 , 0);
		   __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2 , 0);
// generating tabeles in CSV-Format
		  	sprintf(UARTBuffer, "Step;W;X;E;Y\r\n");
		  	len = strlen(UARTBuffer);
// sending the headers via UART
		  	HAL_UART_Transmit(&huart2, (uint8_t*)UARTBuffer, len, 100);
// sending the saved values
		  	for (int var = 0; var < _BUFFERSIZE; ++var)
		  	{
		  		sprintf(UARTBuffer, "%i;%i;%i;%i;%i\r\n", var, wBuffer[var], xBuffer[var], eBuffer[var], yBuffer[var]);
		  		len = strlen(UARTBuffer);
		  		HAL_UART_Transmit(&huart2, (uint8_t*)UARTBuffer, len, 100);
		  	}
		 }
	}
}
